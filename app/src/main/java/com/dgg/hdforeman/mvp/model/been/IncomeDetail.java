public class IncomeDetail {
    private String in_consume;
    private String in_afusable;
    private String in_remark;
    private String create_time;
    private int in_state;
    private int in_type;

    public String getIn_consume() {
        return in_consume;
    }

    public void setIn_consume(String in_consume) {
        this.in_consume = in_consume;
    }

    public String getIn_afusable() {
        return in_afusable;
    }

    public void setIn_afusable(String in_afusable) {
        this.in_afusable = in_afusable;
    }

    public String getIn_remark() {
        return in_remark;
    }

    public void setIn_remark(String in_remark) {
        this.in_remark = in_remark;
    }

    public String getCreate_time() {
        return create_time;
    }

    public void setCreate_time(String create_time) {
        this.create_time = create_time;
    }

    public int getIn_state() {
        return in_state;
    }

    public void setIn_state(int in_state) {
        this.in_state = in_state;
    }

    public int getIn_type() {
        return in_type;
    }

    public void setIn_type(int in_type) {
        this.in_type = in_type;
    }
}
