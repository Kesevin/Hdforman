public class UppacInfo implements Serializable {
    private String uppacid="";//"升级包id"
    private String upmeasure="";//"面积（或数量）"
    private String spaceid="";//"空间id"
    private String spacecname="";//"空间名称"
    private String name="";//"名称"
    private String price="";//"价格"
    private String unit="";//"单位"
    private boolean isCheck = true;//是否选择

    public boolean isCheck() {
        return isCheck;
    }

    public void setCheck(boolean check) {
        isCheck = check;
    }

    public UppacInfo(String name, String price, String unit, String upmeasure, String uppacid) {
        this.unit = unit;
        this.name = name;
        this.price = price;
        this.upmeasure = upmeasure;
        this.uppacid = uppacid;
    }

    public String getUppacid() {
        return uppacid;
    }

    public void setUppacid(String uppacid) {
        this.uppacid = uppacid;
    }

    public String getUpmeasure() {
        return upmeasure;
    }

    public void setUpmeasure(String upmeasure) {
        this.upmeasure = upmeasure;
    }

    public String getSpaceid() {
        return spaceid;
    }

    public void setSpaceid(String spaceid) {
        this.spaceid = spaceid;
    }

    public String getSpacecname() {
        return spacecname;
    }

    public void setSpacecname(String spacecname) {
        this.spacecname = spacecname;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }
}
