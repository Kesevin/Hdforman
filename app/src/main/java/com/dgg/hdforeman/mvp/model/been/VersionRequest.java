public class VersionRequest {
    private String appporttype ;
    private String apptype;

    public VersionRequest(String appporttype, String apptype) {
        this.appporttype = appporttype;
        this.apptype = apptype;
    }
    public VersionRequest() {
    }

    public String getAppporttype() {
        return appporttype;
    }

    public void setAppporttype(String appporttype) {
        this.appporttype = appporttype;
    }

    public String getApptype() {
        return apptype;
    }

    public void setApptype(String apptype) {
        this.apptype = apptype;
    }
}
